    <h4 style="text-align: center;">Enfoques del proyecto:</h4>
    <h5 style="text-align: center;"><strong>SER: Componente Desarrollo personal y social y educacion transversal
    </strong></h5> 
            <p>
            Dentro de este, se integran planes y programas de estudio, considerados socialmente 
            relevantes, pero que atraviesan en forma longitudinal y horizontal al currículo, de 
            tal manera que entorno a ellos se articulan los contenidos correspondientes a las 
            diferentes asignaturas. Los grandes ejes transversales que se consideran pertinentes 
            abordar, para el desarrollo formativo de la mujeres beneficiadas y sus hijos que son 
            realmente afectados por su condición socioeconómica, son: Educación para la salud y 
            Educación para la convivencia pacifica; cada uno de ellos agrupa a su vez distintos 
            temas, el primero contempla la educación afectivo sexual, prevención de las 
            adicciones; el segundo abarca la educación para las relaciones igualitarias entre 
            géneros, la prevención del maltrato y el abuso sexual infantil y la educación para la 
            paz.
            
            Los temas transversales hacen referencia a problemas y conflictos que afectan 
            actualmente a la humanidad, al propio individuo y a su entorno natural, son 
            contenidos con un importante componente actitudinal de valores y normas que pretende 
            ofrecer una formacion integral que acogerá al entorno familiar.
            </p>
    <h5 style="text-align: center;"><strong>SABER: Componente académico gastronomico</strong></h5> 
            <p>La finalidad es dotar a los alumnos del conocimiento y la información para la 
            trasformación de los alimentos y el reconocimiento de las materias primas, así como 
            también los métodos y técnicas necesarias para elaboración de platos novedos e 
            innovadores, incluyendo pasteleria, panaderia y otros; de tal forma que adquieran los
            conocimientos necesarios para poder desempeñarse en restaurantes, cafeterías, bares, 
            fast food, take away, delivery food, vending, centrándose en el servicio, el personal, 
            calidad adecuada y alimentacion saludable y limpia y materias primas saludables y limpias 
            para cada uno de los antes mencionados. 
            Es importante que las beneficiarias comprendan los conceptos básicos sobre higiene y 
            nutrición, adquieran conocimientos sobre las buenas prácticas y Buena alimentacion que
            beneficiara a su hijos en la etapa de crecimiento, donde se precisan sobre todo alimentos
            que proporcionan nutrientes formadores de la estructura y órganos corporales, y donde 
            es importante consumir alimentos energéticos que suministren las calorías necesarias 
            para desarrollar un mayor trabajo físico y más nutrientes reguladores, capaces de 
            normalizar todos los procesos que tienen lugar en el organismo y que  previenen las 
            enfermedades y trastornos específicos que suelen presentarse en cada época por la mala
            alimentación. 
            </p>
    <h5 style="text-align: center;"><strong>HACER: Componente Emprendimiento</strong></h5>        
    <p>
    En este componente se prepara a las beneficiarias a crear y gestionar su propio 
    emprendimiento gastronómico, a lograr interpretar escenarios de mercado y transformarlos 
    en oportunidades de negocios; entender todo el proceso emprendedor y ser autónomas logrando
    incursionar en el mercado laboral y poder mantenerse en el tiempo con proyectos sustentables
    que ellas mismo empoderen.
    </p>
