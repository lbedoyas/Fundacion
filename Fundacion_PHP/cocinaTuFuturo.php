        <li>No pertenecer a grupos delincuenciales de la ciudad</li>
        <li>Vivir en Medellín o su área metropolitana</li>
        <li>No tener antecedentes penales</li>
        <li>Presentar las entrevistas y pruebas de evaluación y psicotécnicas que el programa tiene para hacer la selección de los beneficiarios</li>
        <li>En caso de ser seleccionado firmar el compromiso y cumplir con los manuales de disciplina y convivencia del programa</li>
        <li>Cumplir con recibir la visita domiciliaria con su familia presente</li>
    </ol>
